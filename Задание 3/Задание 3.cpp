#include <iostream>
using namespace std;
int main()
{
    setlocale(LC_ALL, "rus");
    double n, * mass; //объявление переменных
    int sum = 0;
    cout << "Введите количество цифр, которые будут содержаться в массиве: " << endl;
    cin >> n;
    if (n <= 100) //условие
    {
        mass = new double[n];
        cout << "Массив:\n" << endl;
        for (int i = 0; i < n; i++) //цикл для задания рандомных чисел в массиве
        {
            mass[i] = rand() % 101 - 50;
        }
        for (int i = 0; i < n; i++) //цикл для вывода чисел массива
        {
            cout << "mass[" << i << "]=" << mass[i] << "\n" << endl;
        }
        for (int i = 0; i < n; i++) //цикл для подсчета суммы чисел
        {
            sum += mass[i];
        }
        cout << "Сумма действительных чисел массива = " << sum << endl;
    }
    else
    {
        cout << "Вы ввели число больше 100. Завершите программу и попробуйте снова!";
    }
    system("pause");
}

